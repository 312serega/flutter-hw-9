class StaffUserDb {
  final int id;
  final String avatar;
  final String name;
  final String position;
  final String direction;
  final String branch;
  final String email;
  final String birth;
  final String phone;

  StaffUserDb({
    required this.id,
    required this.avatar,
    required this.name,
    required this.position,
    required this.direction,
    required this.branch,
    required this.email,
    required this.birth,
    required this.phone,
  });
}
